package org.example.models;

public class Materia {
    private int idMaterias;
    private String nombreMateria;
    private String detallesMateria;

    public int getIdMaterias() {
        return idMaterias;
    }

    public void setIdMaterias(int idMaterias) {
        this.idMaterias = idMaterias;
    }

    public String getNombreMateria() {
        return nombreMateria;
    }

    public void setNombreMateria(String nombreMateria) {
        this.nombreMateria = nombreMateria;
    }

    public String getDetallesMateria() {
        return detallesMateria;
    }

    public void setDetallesMateria(String detallesMateria) {
        this.detallesMateria = detallesMateria;
    }
}